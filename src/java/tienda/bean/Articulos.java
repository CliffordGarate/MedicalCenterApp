public class Articulos {
   
    private int codArt;
    private String descArt;
    private double precArt;
    private String imgArt;

    public int getCodArt() {
        return codArt;
    }

    public void setCodArt(int codArt) {
        this.codArt = codArt;
    }

    public String getDescArt() {
        return descArt;
    }

    public void setDescArt(String descArt) {
        this.descArt = descArt;
    }

    public double getPrecArt() {
        return precArt;
    }

    public void setPrecArt(double precArt) {
        this.precArt = precArt;
    }

    public String getImgArt() {
        return imgArt;
    }

    public void setImgArt(String imgArt) {
        this.imgArt = imgArt;
    }
    
    
    
    
}
